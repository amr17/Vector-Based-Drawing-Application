    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        MyCanvas canvas = (MyCanvas)jPanel1;
        myShape.setPosition(new Point(Integer.parseInt(jTextField1.getText()), Integer.parseInt(jTextField2.getText())));
        int i=0;
        for(String key : shapeProperties.keySet()){
            if (i==0){ shapeProperties.put(key, Double.parseDouble(jTextField3.getText()));}
            else if (i==1) {shapeProperties.put(key, Double.parseDouble(jTextField4.getText()));}
            else shapeProperties.put(key, Double.parseDouble(jTextField5.getText()));
            i++;
        }
        myShape.setProperties(shapeProperties);
        canvas.addShape(myShape);
        jComboBox1.addItem(myShape.getClass().getSimpleName()+(++counter));
        canvas.repaint();
        jDialog1.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

