    private void undoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_undoBtnActionPerformed
        // TODO add your handling code here:
        MyCanvas canvas = (MyCanvas)jPanel1;
        if(!canvas.deletedShapes.isEmpty()){
           myShape = canvas.deletedShapes.remove(canvas.deletedShapes.size()-1); 
           canvas.addShape(myShape);
           jComboBox1.addItem(myShape.getClass().getSimpleName());
        }
        else if (!canvas.shapes.isEmpty()) return;
        canvas.undo();
        jComboBox1.removeItemAt(jComboBox1.getItemCount()-1);
        canvas.repaint();
    }//GEN-LAST:event_undoBtnActionPerformed

